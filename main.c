void pre_auton() {
  bStopTasksBetweenModes = false;
  bDisplayCompetitionStatusOnLcd = false;
  startTask(handleAll);
  lcdStartup();
  robotConfigure();
  startTask(lcdDebug);
}


task autonomous() {
  writeDebugStreamLine("autonomous()");
  driveReset();
  if (match.type == 0) {
    goAuton(match.routine);
  } else if (match.type == 2) {
    goSkills(match.routine);
  }
}



task usercontrol() {
  writeDebugStreamLine("usercontrol()");
  driveReset();
  while (true) {

    updateState();

    if (match.type == 3) {
      outputStateCode();
    }
    wait1Msec(20);
  }
}
